a = 10  # 0000 1010
b = 18  # 0001 0010

print("Binary of a =", bin(a))
print("Binary of b =", bin(b))
print("a & b =", a & b)  # 0000 0010
print("a | b =", a | b)  # 0001 1010
print("a ^ b =", a ^ b)  # 0001 1000
print("~a =", ~a)  # 1111 0101
print("a >> 1 =", a >> 1)  # 0000 0101
print("b << 2 =", b << 2)  # 0100 1000
